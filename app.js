// Function to create a star
async function createStar(event) {
  event.preventDefault();
  const starName = document.getElementById('starName').value;
  const starId = document.getElementById('starId').value;

  try {
      const accounts = await web3.eth.getAccounts();
      await starNotary.methods.createStar(starName, starId).send({ from: accounts[0] });
      console.log(`Star created: ${starName} with ID ${starId}`);
      updateStarInfo(starId); // Update star information
  } catch (error) {
      console.error(error);
  }
}

// Function to sell a star
async function sellStar(event) {
  event.preventDefault();
  const starId = document.getElementById('saleStarId').value;
  const starPrice = web3.utils.toWei(document.getElementById('starPrice').value, 'ether');

  try {
      const accounts = await web3.eth.getAccounts();
      await starNotary.methods.putStarUpForSale(starId, starPrice).send({ from: accounts[0] });
      console.log(`Star ID ${starId} put up for sale for ${starPrice} Wei`);
      updateStarInfo(starId); // Update star information
  } catch (error) {
      console.error(error);
  }
}

// Function to buy a star
async function buyStar(event) {
  event.preventDefault();
  const starId = document.getElementById('buyStarId').value;

  try {
      const starPrice = await starNotary.methods.starsForSale(starId).call();
      const accounts = await web3.eth.getAccounts();
      await starNotary.methods.buyStar(starId).send({ from: accounts[0], value: starPrice });
      console.log(`Star ID ${starId} bought for ${starPrice} Wei`);
      updateStarInfo(starId); // Update star information
  } catch (error) {
      console.error(error);
  }
}

// Function to exchange stars between two owners
async function exchangeStars(event) {
  event.preventDefault();
  const starId1 = document.getElementById('starId1').value;
  const starId2 = document.getElementById('starId2').value;

  try {
      const accounts = await web3.eth.getAccounts();
      await starNotary.methods.exchangeStars(starId1, starId2).send({ from: accounts[0] });
      console.log(`Stars exchanged: ${starId1} and ${starId2}`);
      updateStarInfo(starId1); // Update star information for the first star
      updateStarInfo(starId2); // Update star information for the second star
  } catch (error) {
      console.error(error);
  }
}

// Function to transfer a star to another address
async function transferStar(event) {
  event.preventDefault();
  const starId = document.getElementById('transferStarId').value;
  const addressTo = document.getElementById('transferAddress').value;

  try {
      const accounts = await web3.eth.getAccounts();
      await starNotary.methods.transferStar(addressTo, starId).send({ from: accounts[0] });
      console.log(`Star ID ${starId} transferred to ${addressTo}`);
      updateStarInfo(starId); // Update star information
  } catch (error) {
      console.error(error);
  }
}

// Function to look up a star's information
async function lookUpStar(event) {
  event.preventDefault();
  const starId = document.getElementById('lookUpStarId').value;

  try {
      // Update star information
      updateStarInfo(starId);
  } catch (error) {
      console.error(error);
  }
}

// Function to update star information on the UI
async function updateStarInfo(tokenId) {
  try {
      const starName = await starNotary.methods.lookUptokenIdToStarInfo(tokenId).call();
      const starOwner = await starNotary.methods.ownerOf(tokenId).call();
      const starPrice = await starNotary.methods.starsForSale(tokenId).call();
      const starPriceInEther = web3.utils.fromWei(starPrice, 'ether');

      starTokenIdInfo.innerText = tokenId;
      starNameInfo.innerText = starName;
      starOwnerInfo.innerText = starOwner;
      starPriceInfo.innerText = starPriceInEther;
  } catch (error) {
      console.error(error);
      // Clear previous data in case of error
      starTokenIdInfo.innerText = '';
      starNameInfo.innerText = '';
      starOwnerInfo.innerText = '';
      starPriceInfo.innerText = '';
  }
}

